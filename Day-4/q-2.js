function multiplyBy(){
    firstNum = document.getElementById("firstNum").value;
    secondNum = document.getElementById("secondNum").value;

    document.getElementById("result").innerHTML = firstNum*secondNum;
}

function divideBy(){
    firstNum = document.getElementById("firstNum").value;
    secondNum = document.getElementById("secondNum").value;

    document.getElementById("result").innerHTML = firstNum/secondNum;
}